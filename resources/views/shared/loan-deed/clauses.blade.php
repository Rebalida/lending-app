            being provided to the Borrower.</li>
    </ol>

    <h3 class="deed-h3">5&nbsp;&nbsp;&nbsp;Repayment and prepayment</h3>

    <h4 class="deed-h4">5.1&nbsp;&nbsp;Payment of Amount Owing</h4>
    <ol type="a" class="deed-ol">
        <li>Borrower must pay the Amount Owing and all other amounts due under this deed by way of
            instalments in the amounts and on the dates set out in the Repayment Schedule</li>
    </ol>

    <h4 class="deed-h4">5.2&nbsp;&nbsp;Prepayment</h4>
    <p class="deed-p">Borrower may repay the Amount Owing in full or part at any time between the first anniversary of
        the Commencement Date and the Repayment Date with the prior written consent of Lender.</p>
    <p class="deed-p">If the Borrower prepays all or any part of the Loan before the scheduled repayment date, the
        Borrower must pay the applicable exit fee and break costs specified in the Schedule, together
        with all other amounts owing under this Agreement.</p>

    <h4 class="deed-h4">5.3&nbsp;&nbsp;Release of Encumbrance</h4>
    <p class="deed-p">Lender will release the Security on receipt in full of the Amount Owing and all other amounts due
        under this deed in cleared funds.</p>

    <h3 class="deed-h3">6&nbsp;&nbsp;&nbsp;Payments generally</h3>

    <h4 class="deed-h4">6.1&nbsp;&nbsp;Manner of payment</h4>
    <p class="deed-p">Unless we agree otherwise each Borrower agrees to make payments (including payment made
        by way of reimbursement) of any amount due and payable under this deed by direct debit from a
        nominated account:</p>
    <ol type="a" class="deed-ol">
        <li>not later than 11 am (Sydney time) on the relevant date;</li>
        <li>in Australian dollars in immediately available funds;</li>
        <li>in full, without set-off or counterclaim and without any deduction in respect of Tax unless prohibited by law; and</li>
        <li>to Lender by payment into the account nominated by them, or by payment as they otherwise direct in writing.</li>
    </ol>

    <h4 class="deed-h4">6.2&nbsp;&nbsp;Direct Debit Arrangement</h4>
    <p class="deed-p">The Borrower agrees to nominate an account in the name of one or more Borrower which is
        held with a Bank or financial institution we approve and from which Lender is authorised to draw
        loan repayments and other amounts Borrower is required to pay Lender under this Deed.
        Borrower must maintain sufficient funds in the nominated account to cover such drawings and
        ensure an up to date direct debit authority is in place, authorising Borrower to debit the
        nominated account. If a direct debit repayment is dishonoured due to lack of funds or for any
        other reason, it will be treated as if it has not been made.</p>

    <h4 class="deed-h4">6.3&nbsp;&nbsp;Grossing up</h4>
    <p class="deed-p">If at any time any applicable law requires a Borrower to make any deduction or withholding in
        respect of Tax from any payment to Lender under this deed, that Borrower must:</p>
    <ol type="a" class="deed-ol">
        <li>notify Lender of the nature of that requirement promptly following that Borrower becoming aware of it;</li>
        <li>ensure that any such deduction or withholding does not exceed the minimum amount legally required;</li>
        <li>pay to the relevant Government Agency the full amount of any such deduction or withholding
            within the time for payment allowed under applicable law;</li>
        <li>pay Lender, at the time that payment is due, such additional amounts as are necessary to ensure
            that, after the making of that deduction or withholding, Lender receives a net sum equal to the sum
            which it would have received had no such deduction or withholding been made;</li>
        <li>indemnify Lender against, and pay Lender on demand the amount of, all claims, losses, liabilities,
            costs and expenses incurred by Lender by reason of any failure of that Borrower to make any such
            deduction or withholding; and</li>
        <li>promptly deliver to Lender copies of any receipts, certificates or other proof evidencing the
            amounts (if any) paid or payable in respect of any such deduction or withholding.</li>
    </ol>

