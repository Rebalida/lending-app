    <h4 class="deed-h4">1.3&nbsp;&nbsp;Sole agreement</h4>
    <p class="deed-p">The covenants and provisions contained in this deed exclusively and completely state the rights
        and obligations of the parties with respect to the Loan. To the extent permitted by law, this
        agreement supersedes all negotiations and prior agreements, whether written or oral, in respect of the Loan.</p>

    <h4 class="deed-h4">1.4&nbsp;&nbsp;Inconsistency</h4>
    <p class="deed-p">If there is any conflict or inconsistency between the provisions of this deed and the provisions
        of any Security, the provisions of this deed will prevail to the extent of that conflict or inconsistency.</p>

    <h4 class="deed-h4">1.5&nbsp;&nbsp;Joint and several liability</h4>
    <p class="deed-p">Where the Borrower comprises more than one person, the covenants, obligations and
        agreements on their part contained or implied in this deed bind them jointly and each of them severally</p>

    <h4 class="deed-h4">1.6&nbsp;&nbsp;Neutral interpretation</h4>
    <p class="deed-p">Nothing in this deed is to be interpreted against a party solely on the grounds that the party put
        forward this deed or a relevant part of it.</p>

    <h3 class="deed-h3">2&nbsp;&nbsp;&nbsp;Loan</h3>

    <h4 class="deed-h4">2.1&nbsp;&nbsp;Advance</h4>
    <p class="deed-p">Lender will advance the Principal Sum to Borrowers in full, on or around the Commencement
        Date and each Borrower agrees that they will borrow the Principal Sum from Lender on the covenants of this deed.</p>

    <h4 class="deed-h4">2.2&nbsp;&nbsp;Use of Loan</h4>
    <p class="deed-p">The Loan may only be used for the Loan Purpose as in the business declaration signed by
        borrower, or such other purpose approved by Lender in writing.</p>

    <h3 class="deed-h3">3&nbsp;&nbsp;&nbsp;Interest</h3>

    <h4 class="deed-h4">3.1&nbsp;&nbsp;Interest accrues</h4>
    <ol type="a" class="deed-ol">
        <li>Interest is calculated daily on the Amount Owing and accrues monthly based on multiplying:
            <ol type="A" class="deed-ol">
                <li>the daily interest rate calculated in accordance with paragraph 3.1(b); by</li>
                <li>the Amount Owing on the relevant date;</li>
            </ol>
            for the period from the Commencement Date until the Amount Owing has been repaid in full.</li>
        <li>The daily interest rate is calculated by dividing the relevant rate by 365. The relevant rate is the
            Higher Rate reducible to the Lower Rate if, and only if no Obligor has committed an unremedied
            Event of Default</li>
    </ol>

    <h4 class="deed-h4">3.2&nbsp;&nbsp;Payment of Interest</h4>
    <ol type="a" class="deed-ol">
        <li>Interest which accrues up to any Interest Payment Date is payable in arrears on the earlier of:
            <ol type="A" class="deed-ol">
                <li>the date on which the Loan Outstanding is repaid in full; and</li>
                <li>the relevant Interest Payment Date,</li>
            </ol>
            and is capitalised, to the extent that it has not been paid, on the relevant Interest Payment Date.</li>
        <li>Interest paid in advance at the time Borrower prepays the Loan will not be refunded.</li>
    </ol>

    <h4 class="deed-h4">3.3&nbsp;&nbsp;Additional interest</h4>
    <p class="deed-p">If a liability under this deed becomes merged in a judgment or order, or exists after any winding
        up of a Borrower, each Obligor must, as an independent obligation, pay interest on the amount
        of that liability from the date that the liability becomes payable, both before and after the judgment,
        order or winding up, until it is paid, at the higher of the rate payable under the judgment, order or
        winding up and the Higher Rate.</p>

    <h4 class="deed-h4">3.4&nbsp;&nbsp;Capitalisation of amounts due in default</h4>
    <ol type="a" class="deed-ol">
        <li>Interest or any other amount unpaid but due and payable capitalises on the date payment is due
            and remains part of the Amount Owing unless or until the relevant payment default is remedied to
            the satisfaction of Lender.</li>
        <li>Nothing in clause 3.4(a) affects an Obligor's obligation to pay an amount which is due and payable
            on the date on which it falls due for payment or the failure to pay being an Event of Default.</li>
    </ol>

