    <p class="deed-p">The Lender's obligation to provide the Loan to, or at the direction of, the Borrower, is (unless
        waived in writing by the lender) subject to and conditional on:</p>
    <ol type="a" class="deed-ol">
        <li>the Borrower delivering to the Lender copies of the certificate of incorporation and constitution and
            any trust deeds of the Borrower, certified to the satisfaction of the Lender as complete, accurate
            and up-to-date;</li>
        <li>the Borrower delivering to the Lender a copy of a resolution of the board of directors of the
            Borrower, certified by the chair as complete and accurate:
            <ol type="A" class="deed-ol">
                <li>approving the terms of this agreement and the Security and all matters required to be done
                    by the Borrower under or pursuant to this agreement and the Security;</li>
                <li>approving the Borrower's entry into, and performance of, this agreement and the Security; and</li>
                <li>appointing appropriate officers as authorised signatories to execute or sign this agreement
                    and the Security on behalf of the Borrower, together with all notices, communications and
                    other documents and instruments required under this agreement and the Security;</li>
            </ol>
        </li>
        <li>the Lender having received evidence satisfactory to the Lender that the Borrower has validly
            entered into, and become bound by the terms of, this agreement and the Security and that this
            agreement and the Security have, where necessary, been duly stamped and registered;</li>
        <li>the Borrower having paid the Establishment Fee (and other amounts payable to the Lender under
            the schedule</li>
        <li>no Event of Default, or event which with the giving of notice or the lapse of time (or both) would be
            an Event of Default, having occurred;</li>
        <li>the representations and warranties of the Borrower contained in clause 10 being true and correct
            in a material respect on the Loan Date;</li>
        <li>The Borrower has provided and shall maintain insurance over the Security in a form, amount and
            with an insurer satisfactory to the Lender.</li>
        <li>no change of circumstance contemplated in having occurred; and</li>
        <li>the Lender having received, in form and substance satisfactory to the Lender, any other
            documents, records or information (including accounts or financial statements) which the Lender
            may request from time to time.</li>
    </ol>

    <h4 class="deed-h4">4.2&nbsp;&nbsp;Non-satisfaction of conditions precedent</h4>
    <p class="deed-p">If each condition precedent set out in clause 4.1 has not been satisfied or expressly waived in
        writing by the Lender, then:</p>
    <ol type="a" class="deed-ol">
        <li>the Lender will not be under any obligation to make the Loan available to the Borrower; and</li>
        <li>the Borrower must, on demand (and in any event, within 3 Business Days) by the Lender, pay to
            and indemnify the Lender against any costs, losses, charges, expenses, liabilities, damages, fees
